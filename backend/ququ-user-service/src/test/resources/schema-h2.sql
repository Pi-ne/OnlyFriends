DROP TABLE IF EXISTS merchant_apply;
DROP TABLE IF EXISTS merchant_info;
DROP TABLE IF EXISTS user_ban_record;
DROP TABLE IF EXISTS `user`;

CREATE TABLE `user` (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email VARCHAR(100) NOT NULL UNIQUE,
  password_hash VARCHAR(255) NOT NULL,
  nickname VARCHAR(50) NOT NULL UNIQUE,
  avatar_url VARCHAR(500),
  gender TINYINT DEFAULT 0,
  birthday DATE,
  bio VARCHAR(200),
  interest_tags JSON,
  user_type TINYINT NOT NULL DEFAULT 0,
  status TINYINT NOT NULL DEFAULT 0,
  credit_score INT NOT NULL DEFAULT 100,
  activate_token VARCHAR(100),
  ban_expire_at TIMESTAMP,
  deleted TINYINT NOT NULL DEFAULT 0,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE merchant_info (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL UNIQUE,
  merchant_name VARCHAR(100) NOT NULL,
  merchant_nick VARCHAR(50),
  focus_tags JSON,
  license_url VARCHAR(500),
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE merchant_apply (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  merchant_name VARCHAR(100) NOT NULL,
  license_url VARCHAR(500) NOT NULL,
  focus_tags JSON,
  status TINYINT NOT NULL DEFAULT 0,
  reject_reason VARCHAR(500),
  reviewer_id BIGINT,
  reviewed_at TIMESTAMP,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE user_ban_record (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  admin_id BIGINT NOT NULL,
  reason VARCHAR(500) NOT NULL,
  ban_expire_at TIMESTAMP,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
