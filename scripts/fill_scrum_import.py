EPIC_BY_ID = {e["id"]: e for e in EPICS}


def story_description(role: str, want: str, so_that: str) -> str:
    return f"作为 {role}\n我想要 {want}\n以便于 {so_that}"


def build_row(
    item_type: str,
    title: str,
    item_id: int,
    parent_id,
    description: str,
    priority: str,
    version: str,
    module: str,
    tags: str = "",
) -> list:
    pri, importance = PRIORITY_MAP[priority]
    start_date, end_date = DATE_RANGE[version]
    return [
        item_type,  # 类型
        title,  # *标题
        item_id,  # 编号
        parent_id,  # 父工作项编号
        None,  # 处理人
        ITERATION_MAP[version],  # 迭代
        module,  # 模块
        pri,  # 优先级
        importance,  # 重要程度
        "新建",  # 状态
        description,  # 描述
        tags,  # 标签
        "功能",  # 领域
        start_date,  # 预计开始日期
        end_date,  # 预计结束日期
        VERSION_MAP[version],  # 发布版本号
        None,  # 预计工时
        None,  # 创建人
        None,  # 创建时间
        None,  # 结束时间
        None,  # 我的自定义文本
        None,  # 自定义字段的选择项
        None,  # 自定义时间
    ]


def main() -> None:
    wb = openpyxl.load_workbook(TEMPLATE)
    ws = wb[wb.sheetnames[0]]

    # Preserve header row style by clearing data rows only
    if ws.max_row > 1:
        ws.delete_rows(2, ws.max_row - 1)

    rows = []

    for epic in EPICS:
        rows.append(
            build_row(
                item_type="epic",
                title=f"{epic['code']} {epic['title']}",
                item_id=epic["id"],
                parent_id=None,
                description=epic["description"],
                priority="P0" if epic["version"] in ("v0.0", "v0.1") else "P1",
                version=epic["version"],
                module=epic["module"],
                tags=epic["code"],
            )
        )

    story_id = 20001
    for code, parent_id, title, role, want, so_that, priority, version in STORIES:
        epic = EPIC_BY_ID[parent_id]
        rows.append(
            build_row(
                item_type="story",
                title=f"{code} {title}",
                item_id=story_id,
                parent_id=parent_id,
                description=story_description(role, want, so_that),
                priority=priority,
                version=version,
                module=epic["module"],
                tags=code,
            )
        )
        story_id += 1

    for row_idx, row_data in enumerate(rows, start=2):
        for col_idx, value in enumerate(row_data, start=1):
            ws.cell(row=row_idx, column=col_idx, value=value)

    wb.save(TEMPLATE)
    print(f"Filled {len(rows)} work items ({len(EPICS)} epics + {len(STORIES)} stories) into {TEMPLATE}")


if __name__ == "__main__":
    main()
